function getNestedValue(obj, path) {
  return path.split(".").reduce((current, key) => current?.[key], obj);
}

function getCurrentLang() {
  const params = new URLSearchParams(window.location.search);
  const fromUrl = params.get("lang");
  const fromStorage = localStorage.getItem("lang");
  const lang = fromUrl || fromStorage || "fr";
  return ["fr", "en", "es"].includes(lang) ? lang : "fr";
}

function setLanguage(lang) {
  const selected = ["fr", "en", "es"].includes(lang) ? lang : "fr";
  const t = translations[selected];

  document.documentElement.lang = selected;

  const pageKey = document.body.dataset.page || "home";
  const title = getNestedValue(t, `pageTitle.${pageKey}`);
  if (title) document.title = title;

  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const value = getNestedValue(t, el.getAttribute("data-i18n"));
    if (value !== undefined) el.textContent = value;
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
    const value = getNestedValue(t, el.getAttribute("data-i18n-placeholder"));
    if (value !== undefined) el.placeholder = value;
  });

  document.querySelectorAll("[data-i18n-option]").forEach((el) => {
    const value = getNestedValue(t, el.getAttribute("data-i18n-option"));
    if (value !== undefined) el.textContent = value;
  });

  document.querySelectorAll(".lang-btn").forEach((btn) => {
    btn.classList.toggle("active", btn.dataset.lang === selected);
    btn.setAttribute("aria-pressed", btn.dataset.lang === selected);
  });

  localStorage.setItem("lang", selected);

  const url = new URL(window.location);
  if (selected === "fr") url.searchParams.delete("lang");
  else url.searchParams.set("lang", selected);
  window.history.replaceState({}, "", url);

  const suffix = selected === "fr" ? "" : `?lang=${selected}`;
  document.querySelectorAll("[data-page-href]").forEach((el) => {
    const raw = el.dataset.pageHref;
    const hashIdx = raw.indexOf("#");
    const path = hashIdx >= 0 ? raw.slice(0, hashIdx) : raw;
    const hash = hashIdx >= 0 ? raw.slice(hashIdx) : "";
    el.href = path + suffix + hash;
  });
}

function langSuffix() {
  const lang = getCurrentLang();
  return lang === "fr" ? "" : `?lang=${lang}`;
}

function initLanguage() {
  setLanguage(getCurrentLang());
  document.querySelectorAll(".lang-btn").forEach((btn) => {
    btn.addEventListener("click", () => setLanguage(btn.dataset.lang));
  });
}
